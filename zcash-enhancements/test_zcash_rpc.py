class TestZkoolSend:
    @pytest.mark.skipif(
        not os.environ.get("ZKOOL_TEST_SEND"),
        reason="Set ZKOOL_TEST_SEND=1 to run send tests (requires funded account)",
    )
    def test_send(self, zkool):
        account_id = int(os.environ.get("ZKOOL_ACCOUNT_ID", "1"))
        to_addr = os.environ.get("ZKOOL_TEST_RECIPIENT", "")
        assert to_addr, "Set ZKOOL_TEST_RECIPIENT env var"
        result = zkool.send(
            account_id=account_id,
            recipients=[{"address": to_addr, "amount": 0.0001}],
            src_pools=4,  # Orchard preferred
        )
        assert result is not None


