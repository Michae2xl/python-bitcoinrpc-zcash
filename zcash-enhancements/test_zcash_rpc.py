# ─── ZebraRPC tests (chain data via zebrad) ──────────────────────────

class TestZebraRPC:
    def test_getblockchaininfo(self, zebra):
        info = zebra.getblockchaininfo()
        assert "chain" in info
        assert "blocks" in info

    def test_getbestblockhash(self, zebra):
        hash_ = zebra.getbestblockhash()
        assert isinstance(hash_, str)
        assert len(hash_) == 64  # 32 bytes hex

    def test_getblock(self, zebra):
        hash_ = zebra.getbestblockhash()
        block = zebra.getblock(hash_, verbosity=1)
        assert "height" in block
        assert "tx" in block

    def test_getblock_by_height(self, zebra):
        block = zebra.getblock(1, verbosity=1)
        assert block["height"] == 1

    def test_z_validateaddress_transparent(self, zebra):
        # t1 mainnet transparent address format check
        result = zebra.z_validateaddress("t1Hsc1LR8yKnbbe3twRp88p6vFfC87hPDFp")
        assert "isvalid" in result

    def test_z_validateaddress_invalid(self, zebra):
        result = zebra.z_validateaddress("invalid_address_xyz")
        assert result.get("isvalid") is False

    def test_getaddressbalance(self, zebra):
        # Query balance for a known address (may be 0)
        result = zebra.getaddressbalance(["t1Hsc1LR8yKnbbe3twRp88p6vFfC87hPDFp"])
        assert "balance" in result

    def test_z_listunifiedreceivers(self, zebra):
        # This test requires a valid UA — skip if no UA available
        # Just verify the method exists and handles errors
        with pytest.raises(Exception):
            zebra.z_listunifiedreceivers("not_a_unified_address")


